# Indexing
#         01234567890123
parrot = "Norwegian Blue"

print(parrot[0:6]) # Norweg
print(parrot[3:5]) # we
print(parrot[0:9]) # Norwegian
print(parrot[:9]) # Norwegian

print(parrot[10:14]) # Blue
print(parrot[10:]) # Blue

print(parrot[:6])
print(parrot[6:])

print(parrot[:6] + parrot[6:])

print(parrot[:])

letters = "abcdefghijklmnopqrstuvwxyz"
print(letters[:4])
print(letters[4:7])
print(letters[7:11])
print(letters[11:16])
print(letters[16:20])
print(letters[20:23])
print(letters[23:])
